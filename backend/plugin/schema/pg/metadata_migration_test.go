func TestPGMetadataDiffEnumObjectChanges(t *testing.T) {
	tests := []struct {
		name     string
		source   []*storepb.EnumTypeMetadata
		target   []*storepb.EnumTypeMetadata
		contains []string
	}{
		{
			name:   "create enum",
			source: nil,
			target: []*storepb.EnumTypeMetadata{
				{
					Name:   "status",
					Values: []string{"active", "archived"},
				},
			},
			contains: []string{`CREATE TYPE "public"."status" AS ENUM`, `'active'`, `'archived'`},
		},
		{
			name: "drop enum",
			source: []*storepb.EnumTypeMetadata{
				{
					Name:   "status",
					Values: []string{"active"},
				},
			},
			target:   nil,
			contains: []string{`DROP TYPE "public"."status"`},
		},
		{
			name: "append enum value",
			source: []*storepb.EnumTypeMetadata{
				{
					Name:   "status",
					Values: []string{"active"},
				},
			},
			target: []*storepb.EnumTypeMetadata{
				{
					Name:   "status",
					Values: []string{"active", "archived"},
				},
			},
			contains: []string{`ALTER TYPE "public"."status" ADD VALUE 'archived'`},
		},
		{
			name: "insert enum value before existing value",
			source: []*storepb.EnumTypeMetadata{
				{
					Name:   "status",
					Values: []string{"active", "archived"},
				},
			},
			target: []*storepb.EnumTypeMetadata{
				{
					Name:   "status",
					Values: []string{"active", "pending", "archived"},
				},
			},
			contains: []string{`ALTER TYPE "public"."status" ADD VALUE 'pending' BEFORE 'archived'`},
		},
		{
			name: "removed enum value emits warning",
			source: []*storepb.EnumTypeMetadata{
				{
					Name:   "status",
					Values: []string{"active", "archived"},
				},
			},
			target: []*storepb.EnumTypeMetadata{
				{
					Name:   "status",
					Values: []string{"active"},
				},
			},
			contains: []string{`-- WARNING: PostgreSQL does not support removing enum values from "public"."status": 'archived'`},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			source := newPGDatabaseMetadataWithEnum(tt.source)
			target := newPGDatabaseMetadataWithEnum(tt.target)

			sql, err := schema.DiffMigration(storepb.Engine_POSTGRES, source, target)

			require.NoError(t, err)
			for _, want := range tt.contains {
				require.Contains(t, sql, want)
			}
		})
	}
}

