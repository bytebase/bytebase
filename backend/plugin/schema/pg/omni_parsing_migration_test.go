// Group 1: ALTER parsing tests (migrated from pg_alter_parsing_test.go)

func TestOmniAlterColumnIdentity(t *testing.T) {
	sql := omniSDLMigration(t, "", `CREATE TABLE products (id integer GENERATED BY DEFAULT AS IDENTITY NOT NULL);`)
	require.Contains(t, sql, "CREATE TABLE")
	require.Contains(t, sql, "products")
}

func TestOmniIdentityEquivalence(t *testing.T) {
	sdl := `CREATE TABLE orders (id integer GENERATED BY DEFAULT AS IDENTITY NOT NULL, amount numeric);`
	sql := omniSDLMigration(t, sdl, sdl)
	require.Empty(t, sql)
}

func TestOmniSequenceOwnedBy(t *testing.T) {
	// Verify that a standalone sequence (without OWNED BY) can be loaded and diffed.
	// The OWNED BY clause requires the referenced table to already exist in the catalog,
	// so we test sequence creation separately and table+sequence together without OWNED BY.
	sql := omniSDLMigration(t, "", `CREATE TABLE users (id integer); CREATE SEQUENCE user_seq;`)
	require.NotEmpty(t, sql)
	require.Contains(t, sql, "user_seq")
}

// Group 2: Extension sync bug tests (migrated from extension_test.go)

func TestOmniExtensionWithVersion(t *testing.T) {
	sdl := `CREATE EXTENSION IF NOT EXISTS "pg_trgm" WITH SCHEMA "public" VERSION '1.6';`
	_, err := catalog.LoadSDL(strings.TrimSpace(sdl))
	require.NoError(t, err)
}

func TestOmniExtensionCommentNoPhantomSchema(t *testing.T) {
	// Verify that loading an extension SDL does not create a phantom schema named after the extension.
	toSDL := `CREATE EXTENSION IF NOT EXISTS "pg_trgm";`
	to, err := catalog.LoadSDL(strings.TrimSpace(toSDL))
	require.NoError(t, err)
	require.NotNil(t, to)

	// Adding a real table alongside the extension should not include a "pg_trgm" schema.
	toSDL2 := `CREATE EXTENSION IF NOT EXISTS "pg_trgm";
CREATE TABLE test_table (id integer PRIMARY KEY);`
	sql := omniSDLMigration(t, "", toSDL2)
	require.NotContains(t, sql, `CREATE SCHEMA "pg_trgm"`)
	require.NotContains(t, sql, "CREATE SCHEMA pg_trgm")
}

func TestOmniExtensionNoDiffWhenIdentical(t *testing.T) {
	sdl := `CREATE EXTENSION IF NOT EXISTS "pg_trgm";`
	sql := omniSDLMigration(t, sdl, sdl)
	require.Empty(t, sql)
}

// Group 3: SDL validation tests (migrated from sdl_validation_testcontainer_test.go)

func TestOmniSDLValidation_CreateFromEmpty(t *testing.T) {
	sql := omniSDLMigration(t, "", `
		CREATE TABLE users (id serial PRIMARY KEY, name varchar(255) NOT NULL, email varchar(255) UNIQUE);
		CREATE TABLE orders (id serial PRIMARY KEY, user_id integer REFERENCES users(id), total decimal(10,2));
		CREATE INDEX idx_orders_user_id ON orders(user_id);
	`)
	require.Contains(t, sql, "users")
	require.Contains(t, sql, "orders")
	require.Contains(t, sql, "idx_orders_user_id")
}

func TestOmniSDLValidation_AddViewAndFunction(t *testing.T) {
	fromSDL := `CREATE TABLE users (id serial PRIMARY KEY, name text NOT NULL, active boolean DEFAULT true);`
	toSDL := fromSDL + `
		CREATE VIEW active_users AS SELECT id, name FROM users WHERE active = true;
		CREATE FUNCTION count_active() RETURNS integer LANGUAGE sql AS $$ SELECT count(*) FROM users WHERE active = true $$;
	`
	sql := omniSDLMigration(t, fromSDL, toSDL)
	require.Contains(t, sql, "VIEW")
	require.Contains(t, sql, "FUNCTION")
}

func TestOmniSDLValidation_DropAllObjects(t *testing.T) {
	fromSDL := `CREATE TABLE t1 (id serial PRIMARY KEY); CREATE TABLE t2 (id serial PRIMARY KEY, ref integer REFERENCES t1(id));`
	sql := omniSDLMigration(t, fromSDL, "")
	require.Contains(t, sql, "DROP")
}

func TestOmniSDLValidation_CrossSchemaReferences(t *testing.T) {
	sql := omniSDLMigration(t, "", `
		CREATE SCHEMA app;
		CREATE TABLE app.users (id serial PRIMARY KEY, name text NOT NULL);
		CREATE TABLE app.orders (id serial PRIMARY KEY, user_id integer REFERENCES app.users(id));
	`)
	require.Contains(t, sql, "CREATE SCHEMA")
	require.Contains(t, sql, "CREATE TABLE")
}
