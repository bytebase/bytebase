// legacyListColumnQuery is the previous INFORMATION_SCHEMA.COLUMNS-based column query.
// It is kept here as the oracle for TestListColumnQueryMatchesInformationSchema: the
// pg_catalog-based query must produce identical rows when the user has full privileges.
var legacyListColumnQuery = `
SELECT
	cols.table_schema,
	cols.table_name,
	cols.column_name,
	cols.data_type,
	cols.character_maximum_length,
	cols.numeric_precision,
	cols.numeric_scale,
	cols.datetime_precision,
	cols.ordinal_position,
	cols.column_default,
	cols.is_nullable,
	cols.collation_name,
	cols.udt_schema,
	cols.udt_name,
	cols.identity_generation,
	pg_catalog.col_description(c.oid, cols.ordinal_position::int) as column_comment
FROM INFORMATION_SCHEMA.COLUMNS AS cols
LEFT JOIN pg_namespace n ON n.nspname = cols.table_schema
LEFT JOIN pg_class c ON c.relname = cols.table_name AND c.relnamespace = n.oid AND c.relkind IN ('r', 'v', 'm', 'p', 'f')` + fmt.Sprintf(`
WHERE cols.table_schema NOT IN (%s)
  AND cols.table_schema NOT LIKE 'pg_temp%%'
  AND cols.table_schema NOT LIKE 'pg_toast%%'
ORDER BY cols.table_schema, cols.table_name, cols.ordinal_position;`, pgparser.SystemSchemaWhereClause)

type columnQueryRow struct {
	TableSchema        string
	TableName          string
	ColumnName         string
	DataType           string
	CharacterMaxLength sql.NullString
	NumericPrecision   sql.NullString
	NumericScale       sql.NullString
	DatetimePrecision  sql.NullString
	OrdinalPosition    int64
	ColumnDefault      sql.NullString
	IsNullable         string
	CollationName      sql.NullString
	UdtSchema          string
	UdtName            string
	IdentityGeneration sql.NullString
	ColumnComment      sql.NullString
}

func queryColumnRows(t *testing.T, txn *sql.Tx, query string) []columnQueryRow {
	t.Helper()
	rows, err := txn.Query(query)
	require.NoError(t, err)
	defer rows.Close()

	var result []columnQueryRow
	for rows.Next() {
		var r columnQueryRow
		require.NoError(t, rows.Scan(
			&r.TableSchema,
			&r.TableName,
			&r.ColumnName,
			&r.DataType,
			&r.CharacterMaxLength,
			&r.NumericPrecision,
			&r.NumericScale,
			&r.DatetimePrecision,
			&r.OrdinalPosition,
			&r.ColumnDefault,
			&r.IsNullable,
			&r.CollationName,
			&r.UdtSchema,
			&r.UdtName,
			&r.IdentityGeneration,
			&r.ColumnComment,
		))
		result = append(result, r)
	}
	require.NoError(t, rows.Err())

	// The two queries may order rows differently for exotic identifier names
	// (information_schema sorts varchar with the database collation, pg_catalog sorts
	// the name type with C collation), so normalize the order before comparing.
	slices.SortFunc(result, func(a, b columnQueryRow) int {
		if c := strings.Compare(a.TableSchema, b.TableSchema); c != 0 {
			return c
		}
		if c := strings.Compare(a.TableName, b.TableName); c != 0 {
			return c
		}
		return int(a.OrdinalPosition - b.OrdinalPosition)
	})
	return result
}

// TestListColumnQueryMatchesInformationSchema verifies that the pg_catalog-based column
// query produces exactly the same rows as the previous INFORMATION_SCHEMA.COLUMNS-based
// query for a privileged user, across a wide range of column types. This guards the type
// rendering contract: the Go-side formatting in getTableColumns depends on the exact
// data_type / precision / udt values that information_schema used to produce.
func TestListColumnQueryMatchesInformationSchema(t *testing.T) {
	ctx := context.Background()

	pgContainer := testcontainer.GetTestPgContainer(ctx, t)
	defer pgContainer.Close(ctx)

	pgDB := pgContainer.GetDB()
	require.NoError(t, pgDB.Ping())

	setupSQL := `
CREATE SCHEMA app;
CREATE TYPE app.mood AS ENUM ('happy', 'sad');
CREATE TYPE public.shirt_size AS ENUM ('s', 'm', 'l');
CREATE DOMAIN app.positive_int AS integer NOT NULL CHECK (VALUE > 0);
CREATE DOMAIN public.us_zip AS varchar(10);
CREATE DOMAIN app.int_array AS integer[];
CREATE SEQUENCE app.seq1;

CREATE TABLE app.kitchen_sink (
    id bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    id2 integer GENERATED BY DEFAULT AS IDENTITY,
    c_smallint smallint,
    c_int integer NOT NULL DEFAULT 42,
    c_bigint bigint DEFAULT nextval('app.seq1'),
    c_numeric numeric,
    c_numeric_p numeric(8),
    c_numeric_p0 numeric(8, 0),
    c_numeric_ps numeric(10, 2),
    c_real real,
    c_double double precision,
    c_money money,
    c_char char,
    c_char_n char(5),
    c_varchar varchar,
    c_varchar_n varchar(20) DEFAULT 'hello',
    c_text text COLLATE "C",
    c_bytea bytea,
    c_bool boolean DEFAULT true,
    c_date date,
    c_time time,
    c_time_p time(3),
    c_timetz time with time zone,
    c_timetz_p time(5) with time zone,
    c_ts timestamp,
    c_ts_p timestamp(2),
    c_tstz timestamp with time zone DEFAULT now(),
    c_tstz_p timestamp(4) with time zone,
    c_interval interval,
    c_interval_p interval(3),
    c_uuid uuid,
    c_json json,
    c_jsonb jsonb DEFAULT '{}'::jsonb,
    c_xml xml,
    c_inet inet,
    c_cidr cidr,
    c_macaddr macaddr,
    c_bit bit,
    c_bit_n bit(3),
    c_varbit varbit,
    c_varbit_n varbit(8),
    c_int_array integer[],
    c_text_matrix text[][],
    c_varchar_array varchar(5)[],
    c_enum app.mood DEFAULT 'happy',
    c_enum_public public.shirt_size,
    c_enum_array app.mood[],
    c_domain app.positive_int,
    c_domain_varchar public.us_zip DEFAULT 'x',
    c_domain_array app.int_array,
    c_point point,
    c_tsvector tsvector,
    c_int4range int4range,
    c_oid oid,
    c_name name,
    c_internal_char "char",
    c_generated integer GENERATED ALWAYS AS (c_int * 2) STORED,
    c_drop_me text
);
ALTER TABLE app.kitchen_sink DROP COLUMN c_drop_me;
COMMENT ON COLUMN app.kitchen_sink.c_int IS 'int comment';

CREATE TABLE app.parted (id integer, region text NOT NULL) PARTITION BY LIST (region);
CREATE TABLE app.parted_us PARTITION OF app.parted FOR VALUES IN ('us');

CREATE VIEW app.v_sink AS SELECT id, c_int, c_text FROM app.kitchen_sink;

CREATE TABLE public.plain (a serial, b text);
`
	_, err := pgDB.Exec(setupSQL)
	require.NoError(t, err)

	txn, err := pgDB.Begin()
	require.NoError(t, err)
	defer txn.Rollback()

	// SyncDBSchema clears the search path before the column sync; match it so default
	// expressions render identically.
	require.NoError(t, setTxSearchPath(txn, ""))

	legacyRows := queryColumnRows(t, txn, legacyListColumnQuery)
	newRows := queryColumnRows(t, txn, getListColumnQuery(true))

	// Sanity: the kitchen sink table alone has 50+ columns.
	require.Greater(t, len(legacyRows), 50)
	require.Equal(t, legacyRows, newRows)
}

// TestSyncColumnsWithoutTablePrivilege verifies that column sync no longer depends on the
// sync user's table privileges. information_schema.columns privilege-filters its rows, so
// a sync user without privileges on a table used to get table metadata with indexes and
// constraints (read from pg_catalog) but zero columns. The pg_catalog-based column query
// must return the columns regardless.
func TestSyncColumnsWithoutTablePrivilege(t *testing.T) {
	ctx := context.Background()

	pgContainer := testcontainer.GetTestPgContainer(ctx, t)
	defer pgContainer.Close(ctx)

	pgDB := pgContainer.GetDB()
	require.NoError(t, pgDB.Ping())

	setupSQL := `
CREATE SCHEMA restricted;
CREATE TABLE restricted.secret_table (
    id integer PRIMARY KEY,
    name varchar(50) NOT NULL DEFAULT 'anon',
    amount numeric(10, 2),
    CONSTRAINT positive_amount CHECK (amount >= 0)
);
CREATE INDEX idx_secret_name ON restricted.secret_table (name);
CREATE USER limited_user WITH LOGIN PASSWORD 'limited-password';
-- pg_read_all_stats lets the sync read pg_table_size/pg_indexes_size; it grants no
-- table or column privileges, so information_schema.columns still hides the table.
GRANT pg_read_all_stats TO limited_user;
`
	_, err := pgDB.Exec(setupSQL)
	require.NoError(t, err)

	limitedDB, err := sql.Open("pgx", fmt.Sprintf("host=%s port=%s user=limited_user password=limited-password database=postgres", pgContainer.GetHost(), pgContainer.GetPort()))
	require.NoError(t, err)
	defer limitedDB.Close()
	require.NoError(t, limitedDB.Ping())

	// Confirm the privilege filtering this fix works around: information_schema.columns
	// returns nothing for the table when queried by the unprivileged user.
	var count int
	require.NoError(t, limitedDB.QueryRow(`
		SELECT COUNT(*) FROM information_schema.columns
		WHERE table_schema = 'restricted' AND table_name = 'secret_table'`).Scan(&count))
	require.Zero(t, count)

	driver := &Driver{}
	config := db.ConnectionConfig{
		DataSource: &storepb.DataSource{
			Type:     storepb.DataSourceType_ADMIN,
			Username: "limited_user",
			Host:     pgContainer.GetHost(),
			Port:     pgContainer.GetPort(),
			Database: "postgres",
		},
		Password: "limited-password",
		ConnectionContext: db.ConnectionContext{
			EngineVersion: "16.0",
			DatabaseName:  "postgres",
		},
	}

	openedDriver, err := driver.Open(ctx, storepb.Engine_POSTGRES, config)
	require.NoError(t, err)
	defer openedDriver.Close(ctx)

	pgDriver, ok := openedDriver.(*Driver)
	require.True(t, ok)

	metadata, err := pgDriver.SyncDBSchema(ctx)
	require.NoError(t, err)

	var secretTable *storepb.TableMetadata
	for _, schema := range metadata.Schemas {
		if schema.Name != "restricted" {
			continue
		}
		for _, table := range schema.Tables {
			if table.Name == "secret_table" {
				secretTable = table
				break
			}
		}
	}
	require.NotNil(t, secretTable, "restricted.secret_table not found in synced metadata")

	// Indexes and check constraints were always visible via pg_catalog; columns must now
	// be visible too instead of leaving inconsistent table metadata.
	require.NotEmpty(t, secretTable.Indexes)
	require.NotEmpty(t, secretTable.CheckConstraints)

	require.Len(t, secretTable.Columns, 3)
	id, name, amount := secretTable.Columns[0], secretTable.Columns[1], secretTable.Columns[2]

	require.Equal(t, "id", id.Name)
	require.Equal(t, "integer", id.Type)
	require.False(t, id.Nullable)

	require.Equal(t, "name", name.Name)
	require.Equal(t, "character varying(50)", name.Type)
	require.False(t, name.Nullable)
	require.Equal(t, "'anon'::character varying", name.Default)

	require.Equal(t, "amount", amount.Name)
	require.Equal(t, "numeric(10,2)", amount.Type)
	require.True(t, amount.Nullable)
}
