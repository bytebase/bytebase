// Package snowflake is the advisor for snowflake database.
package snowflake

import (
	"fmt"
	"regexp"
	"strings"

	"github.com/antlr4-go/antlr/v4"
	parser "github.com/bytebase/snowsql-parser"
	"go.uber.org/zap"

	"github.com/bytebase/bytebase/backend/common/log"
	"github.com/bytebase/bytebase/backend/plugin/advisor"
	"github.com/bytebase/bytebase/backend/plugin/advisor/db"
)

var (
	_ advisor.Advisor = (*NamingTableAdvisor)(nil)
)

func init() {
	advisor.Register(db.Snowflake, advisor.SnowflakeNamingTableConvention, &NamingTableAdvisor{})
}

// NamingTableAdvisor is the advisor checking for table naming convention.
type NamingTableAdvisor struct {
}

// Check checks for table naming convention.
func (*NamingTableAdvisor) Check(ctx advisor.Context, statement string) ([]advisor.Advice, error) {
	tree, errAdvice := parseStatement(statement)
	if errAdvice != nil {
		return errAdvice, nil
	}

	level, err := advisor.NewStatusBySQLReviewRuleLevel(ctx.Rule.Level)
	if err != nil {
		return nil, err
	}
	format, maxLength, err := advisor.UnmarshalNamingRulePayloadAsRegexp(ctx.Rule.Payload)
	if err != nil {
		return nil, err
	}

	listener := &namingTableListener{
		level:     level,
		title:     string(ctx.Rule.Type),
		format:    format,
		maxLength: maxLength,
	}

	antlr.ParseTreeWalkerDefault.Walk(listener, tree)

	return listener.generateAdvice()
}

type namingTableListener struct {
	*parser.BaseSnowflakeParserListener

	level     advisor.Status
	title     string
	format    *regexp.Regexp
	maxLength int

	adviceList []advisor.Advice
}

// generateAdvice returns the advices generated by the listener, the advices must not be empty.
func (l *namingTableListener) generateAdvice() ([]advisor.Advice, error) {
	if len(l.adviceList) == 0 {
		l.adviceList = append(l.adviceList, advisor.Advice{
			Status:  advisor.Success,
			Code:    advisor.Ok,
			Title:   "OK",
			Content: "",
		})
	}
	return l.adviceList, nil
}

// EnterCreate_table is called when production create_table is entered.
func (l *namingTableListener) EnterCreate_table(ctx *parser.Create_tableContext) {
	tableName := extractTableNameFromIdentifier(ctx.Object_name().GetText())

	if !l.format.MatchString(tableName) {
		l.adviceList = append(l.adviceList, advisor.Advice{
			Status:  l.level,
			Code:    advisor.NamingTableConventionMismatch,
			Title:   l.title,
			Content: fmt.Sprintf(`"%s" mismatches table naming convention, naming format should be %q`, tableName, l.format),
			Line:    ctx.GetStart().GetLine(),
		})
	}
	if l.maxLength > 0 && len(tableName) > l.maxLength {
		l.adviceList = append(l.adviceList, advisor.Advice{
			Status:  l.level,
			Code:    advisor.NamingTableConventionMismatch,
			Title:   l.title,
			Content: fmt.Sprintf("\"%s\" mismatches table naming convention, its length should be within %d characters", tableName, l.maxLength),
			Line:    ctx.GetStart().GetLine(),
		})
	}
}

// EnterAlter_table is called when production alter_table is entered.
func (l *namingTableListener) EnterAlter_table(ctx *parser.Alter_tableContext) {
	if ctx.RENAME() == nil {
		return
	}

	allObjectNames := ctx.AllObject_name()
	if len(allObjectNames) != 2 {
		log.Warn("Unexpected number of object names in alter table rename statement", zap.Int("objectNameCount", len(allObjectNames)))
		return
	}

	newObjectName := allObjectNames[1].GetText()
	parts := strings.Split(newObjectName, ".")
	if len(parts) == 0 {
		log.Warn("Unexpected zero part after splitting snowflake object name with dot", zap.String("objectName", newObjectName))
		return
	}
	tableName := parts[len(parts)-1]
	if tableName[0] == '"' && tableName[len(tableName)-1] == '"' {
		tableName = tableName[1 : len(tableName)-1]
		tableName = strings.ReplaceAll(tableName, `""`, `"`)
	}

	if !l.format.MatchString(tableName) {
		l.adviceList = append(l.adviceList, advisor.Advice{
			Status:  l.level,
			Code:    advisor.NamingTableConventionMismatch,
			Title:   l.title,
			Content: fmt.Sprintf(`"%s" mismatches table naming convention, naming format should be %q`, tableName, l.format),
			Line:    ctx.GetStart().GetLine(),
		})
	}
	if l.maxLength > 0 && len(tableName) > l.maxLength {
		l.adviceList = append(l.adviceList, advisor.Advice{
			Status:  l.level,
			Code:    advisor.NamingTableConventionMismatch,
			Title:   l.title,
			Content: fmt.Sprintf("\"%s\" mismatches table naming convention, its length should be within %d characters", tableName, l.maxLength),
			Line:    ctx.GetStart().GetLine(),
		})
	}
}
